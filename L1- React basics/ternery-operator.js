/*

Ternary Operator (?:)
value1 ? value2 : value3

Works like an if-else statement
If value1 is true then the result is ➡️ value2
Else the result is ➡️ value3

Ternary operator = let us insert an if-else statement directly in the JSX

*/

let age = 20;

let message = (age >= 18) ? "You are an adult" : "You are a minor";
console.log(message);
